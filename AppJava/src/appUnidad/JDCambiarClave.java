    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (String.valueOf(txtClave.getPassword()).equals(usuario.getClave())) {
                if (Arrays.equals(txtNuevaClave.getPassword(), txtConfirmacion.getPassword())) {
                    usuario.cambiarClave(String.valueOf(txtNuevaClave.getPassword()));
                    JOptionPane.showMessageDialog(this, "Contrase単a cambiada correctamente");
                    this.dispose();
                }
                else 
                    JOptionPane.showMessageDialog(this, "Las contrase単as no coinciden");
            } else
                JOptionPane.showMessageDialog(this, "La contrase単a es incorrecta");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

